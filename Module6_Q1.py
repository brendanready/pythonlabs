# 1. What function is used to get the ASCII code of a character?

print('The function that is used to get the ASCII code of a character is "ord()"')
print('ASCII code is basically used for identifying letters, numbers, and special characters in a keyboard')
print('For example, the ASCII value of "!" is', ord('!'))
print('The ASCII value of "$" is', ord('$'))
print('The ASCII value of "1" is', ord('1'))
print('The ASCII value of "9" is', ord('9'))
print('The ASCII value of "A" is', ord('A'))
print('The ASCII value of "z" is', ord('z'))


