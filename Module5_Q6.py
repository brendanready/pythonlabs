# 6. Write a simple function (area_circle) that returns the area of a circle of a given radius.

from math import pi

def area_circle(radius):
    return pi * (radius ** 2)

print('The area of a circle with a radius of 4 units is:', area_circle(4))
print('The area of a circle with a radius of 8 units is:', area_circle(8))


